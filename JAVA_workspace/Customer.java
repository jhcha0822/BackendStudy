public class Customer{
    private String name;
    private int age;
    private boolean isMarry;

	public getName(){
		return name;
	}

	public setName(String name){
		this.name = name;
	}

	public getAge(){
		return age;
	}

	public setName(int age){
		this.age = age;
	}

	public getIsMarry(){
		return isMarry;
	}

	public setIsMarry(boolean isMarry){
		this.isMarry = isMarry;
	}
}
